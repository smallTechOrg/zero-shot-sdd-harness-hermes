def generate(
    project_name: str,
    dest_root: Path,
    description: str,
    skip_deps: bool,
) -> Path:
    project_dir = dest_root / project_name
    if project_dir.exists():
        raise SystemExit(f"Destination already exists: {project_dir}")

    project_dir.mkdir(parents=True)
    slug = _slug(project_name)
    substitutions = {
        "{{PROJECT_NAME}}": project_name,
        "{{PROJECT_SLUG}}": slug,
        "{{PROJECT_DESC}}": description,
    }

    def write(rel: str, content: str) -> None:
        (project_dir / rel).parent.mkdir(parents=True, exist_ok=True)
        text = _substitute(project_name, slug, description, content)
        (project_dir / rel).write_text(text, encoding="utf-8")

    write("README.md", _readme(project_name, slug))
    write(".env.example", _env_example())
    write("docker-compose.yml", _docker_compose())
    write("Dockerfile.backend", _dockerfile_backend(slug))
    write("Dockerfile.frontend", _dockerfile_frontend())
    write("backend/pyproject.toml", _pyproject())
    write("backend/requirements.txt", _requirements())
    write("backend/app/__init__.py", "")
    write("backend/app/main.py", _backend_main_py())
    write("backend/app/config.py", _backend_config())
    write("backend/app/db.py", _backend_db())
    write("backend/app/models.py", _backend_models())
    write("backend/app/agent.py", _backend_agent())
    write("backend/app/routers/__init__.py", _backend_init())
    write("backend/app/routers/health.py", _backend_health_router())
    write("backend/app/routers/chat.py", _backend_chat_router())
    write("backend/app/routers/__init__.py", _backend_init())
    write("backend/tests/__init__.py", "")
    write("backend/tests/test_health.py", _health_test())
    write("backend/tests/test_chat.py", _chat_test())
    write("backend/alembic.ini", _backend_alembic_ini())
    write("backend/alembic/env.py", _backend_alembic_env(slug))
    write("backend/alembic/versions/.gitkeep", "")

    write("frontend/package.json", _package_json(slug))
    write("frontend/vite.config.ts", _vite_config())
    write("frontend/tsconfig.json", _tsconfig_json())
    write("frontend/tsconfig.node.json", _tsconfig_node())
    write("frontend/tailwind.config.js", _tailwind_config())
    write("frontend/postcss.config.js", _postcss_config())
    write("frontend/index.html", _index_html())
    write("frontend/README.md", _readme_frontend())
    write("frontend/src/main.tsx", _main_tsx())
    write("frontend/src/App.tsx", _app_tsx(project_name))
    write("frontend/src/index.css", _index_css())
    write("frontend/src/vite-env.d.ts", _vite_env_dts())
    write("frontend/public/.gitkeep", "")

    if not skip_deps:
        _install_deps(project_dir, slug)

    return project_dir


def _slug(name: str) -> str:
    return re.sub(r"[^a-z0-9-]+", "-", name.lower()).strip("-") or "my-project"


