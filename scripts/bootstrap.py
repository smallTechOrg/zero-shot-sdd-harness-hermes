# ---------------------------------------------------------------------------
# Substitution helpers
# ---------------------------------------------------------------------------
def _substitute(project_name: str, slug: str, description: str, content: str) -> str:
    # Replace exact {{VAR}} placeholders using plain str.replace to avoid
    # conflicts with JSX/CSS braces or Template $ sign rules.
    content = content.replace("{{PROJECT_NAME}}", project_name)
    content = content.replace("{{PROJECT_SLUG}}", slug)
    content = content.replace("{{PROJECT_DESC}}", description)
    # Remove any leftover placeholders
    content = re.sub(r"\{\{[A-Z0-9_]+\}\}", "", content)
    return content


def _write(dest: Path, content: str) -> None:
    dest.parent.mkdir(parents=True, exist_ok=True)
    dest.write_text(content, encoding="utf-8")


