def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="scaffold-agent project generator")
    parser.add_argument("project_name", help="Directory name for the generated project.")
    parser.add_argument("--out", type=Path, default=None, help="Output parent directory (default: sibling of this repo).")
    parser.add_argument("--description", default=DEFAULT_DESCRIPTION, help="Project description.")
    parser.add_argument("--skip-deps", action="store_true", help="Skip optional dependency installation.")
    parser.add_argument("--dry-run", action="store_true", help="Print files without writing them.")
    args = parser.parse_args(argv)

    dest_parent = args.out
    if dest_parent is None:
        dest_parent = PROJECT_ROOT.parent

    dest = dest_parent.resolve()
    print(f"[gen] output root: {dest}")
    print(f"[gen] project: {args.project_name}\n")

    if args.dry_run:
        for rel, content in TEMPLATES.items():
            rendered = _substitute(args.project_name, _slug(args.project_name), args.description, content)
            print(f"--- {rel} ---\n{rendered}\n")
        return 0

    out = generate(
        project_name=args.project_name,
        dest_root=dest,
        description=args.description,
        skip_deps=args.skip_deps,
    )
    print(f"\n[gen] created: {out}")
    print("Next steps:")
    print(f"  cd {out.parent.name if out.parent != PROJECT_ROOT.parent else out.name}")
    print("  docker compose up --build")
    print("  open http://localhost:5173")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
